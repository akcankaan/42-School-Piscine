char	*ft_strcat(char *dest, char *src)
{
	int	x;
	int	y;

	x = 0;
	while (dest[x])
		x++;
	y = 0;
	while (src[y] != '\0')
	{
		dest[x] = src[y];
		x++;
		y++;
	}
	dest[x] = '\0';
	return (dest);
}
#include <stdio.h>
int main()
{
	char dest[] = "mine";
	char src[] = "craft";
	printf("%s", ft_strcat(dest, src));
}
